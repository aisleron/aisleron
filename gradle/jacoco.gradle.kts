import org.gradle.testing.jacoco.tasks.JacocoReport

val coverageExcludes = listOf(
        "**/R.class", "**/R$*.class", "**/BuildConfig.*", "**/Manifest$*.*",
        "**/airbnb/**/*.*", "**/*Screen*", "**/Showkase*",
        "**/theme/Theme*", "**/theme/Type*"
)

fun JacocoReport.configureReport() {
    group = "verification"

    reports {
        html.required = true
        html.outputLocation.set(layout.buildDirectory.dir("reports/jacoco/codeCoverage/html"))
    }

    // Class files generated by the Kotlin compiler.
    classDirectories.setFrom(
        fileTree(layout.buildDirectory.dir("intermediates/built_in_kotlinc/debug/compileDebugKotlin/classes")) {
            exclude(coverageExcludes)
        }
    )

    // Source sets that coverage should be reported for.
    sourceDirectories.setFrom(files("$projectDir/src/main/kotlin"))

    // Collection of execution data files to analyze.
    executionData.setFrom(
        fileTree(layout.buildDirectory) {
            include("outputs/**/*.exec", "outputs/**/*.ec")
        }
    )

    onlyIf {
        executionData.files.any { it.exists() }
    }
}

tasks.register<JacocoReport>("codeCoverage") {
    // Run with ./gradlew codeCoverage
    description = "Generates coverage report from existing execution data."
    configureReport()
}

tasks.register<JacocoReport>("testsWithCodeCoverage") {
    // Run with ./gradlew testsWithCodeCoverage
    description = "Runs all tests and then generates the coverage report."

    dependsOn(tasks.named("testDebugUnitTest"))
    dependsOn(tasks.named("connectedDebugAndroidTest"))

    configureReport()
}